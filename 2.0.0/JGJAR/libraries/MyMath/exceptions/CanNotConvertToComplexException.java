package JGJAR.libraries.MyMath.exceptions;

public class CanNotConvertToComplexException extends Exception
{
	public CanNotConvertToComplexException()
	{
		super();
		
	}
	public CanNotConvertToComplexException(String message)
	{
		super(message);
		
	}
	public CanNotConvertToComplexException(Throwable cause)
	{
		super(cause);
		
	}
	public CanNotConvertToComplexException(String message, Throwable cause)
	{
		super(message, cause);
		
	}
	protected CanNotConvertToComplexException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) 
	{
		super(message, cause, enableSuppression, writableStackTrace);
		
	}
}
