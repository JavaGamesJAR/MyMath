package JGJAR.libraries.MyMath.exceptions;

public class ArraysAreNotSameSizeException extends Exception
{
	public ArraysAreNotSameSizeException()
	{
		super();
		
	}
	public ArraysAreNotSameSizeException(String message)
	{
		super(message);
		
	}
	public ArraysAreNotSameSizeException(Throwable cause)
	{
		super(cause);
		
	}
	public ArraysAreNotSameSizeException(String message, Throwable cause)
	{
		super(message, cause);
		
	}
	protected ArraysAreNotSameSizeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) 
	{
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
